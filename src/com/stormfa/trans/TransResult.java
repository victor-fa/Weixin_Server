package com.stormfa.trans;

public class TransResult {
	private String from;
	private String to;
	private Data src;
	private String dst;

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public Data getSrc() {
		return src;
	}

	public void setSrc(Data src) {
		this.src = src;
	}

	public String getDst() {
		return dst;
	}

	public void setDst(String dst) {
		this.dst = dst;
	}

}
